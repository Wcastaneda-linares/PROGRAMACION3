public class MisMunicipios {
    
    public String jutiapaMunicipios(char cr){
           String Municipios = "";
        switch (cr) {
            case 'a':
                Municipios = "Jutiapa";
                break;
            case 'b':
                Municipios = "El Progreso";
                break;
            case 'c':
                Municipios = "Santa Catarina Mita";
                break;
            case 'd':
                Municipios = "Agua Blanca";
                break;
            case 'e':
                Municipios = "Asunción Mita";
                break;
            case 'f':
                Municipios = "Yupiltepeque";
                break;
            case 'g':
                Municipios = "Atescatempa";
                break;
            case 'h':
                Municipios = "Jerez";
                break;
            case 'i':
                Municipios = "El Adelanto";
                break;
            case 'j':
                Municipios = "Zapotitlán";
                break;
            case 'k':
                Municipios = "Comapa";
                break;
            case 'l':
                Municipios = "Jalpatagua";
                break;
            case 'm':
                Municipios = "Conguaco";
                break;
            case 'n':
                Municipios = "Moyuta";
                break;
            case 'o':
                Municipios = "Pasaco";
                break;
            case 'p':
                Municipios = "San José Acatempa";
                break;
            case 'q':
                Municipios = "Quezada";
                break;
        }
        
        return Municipios;
        
    }
    
 
}
